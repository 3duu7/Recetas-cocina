<h1>Ensalada de alubias</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 2 personas:</p></strong>
				<p>300 g de alubias cocidas</p>
				<p>1 huevo</p>
				<p>1/4 cebolla blanca</p>
				<p>1/4 pimiento rojo</p>
				<p>1/4 pimiento verde</p>
				<p>100 ml de aceite de oliva virgen extra</p>
				<p>1 cucharada de vinagre de manzana</p>
	</div>
	
<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Calentamos abundante agua en un cazo y, cuando arranque el hervor, sumergimos el huevo y lo cocemos durante 12 minutos.</p>

			<p> Retiramos y refrescamos en abundante agua helada antes de pelar.</p>

			<p> Preparamos una vinagreta batiendo, hasta emulsionar, el aceite de oliva con el vinagre  y un poco de sal.</p>

			<p> Reservamos. Lavamos y escurrimos bien las alubias.</p>
			
			<p> Picamos la cebolla, los dos tipos de pimiento y los añadimos a las alubias. Aliñamos con la vinagreta al gusto y servimos con medio huevo duro por ración.</p>
	</div>
</main>