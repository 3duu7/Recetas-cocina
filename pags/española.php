<h1>Española</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
				<p>40 gr de harina </p>
				<p>40 gr de mantequilla o aceite de oliva y<br> 3/4 de litro de fondo oscuro</p>
			<p>Para enriquecer la salsa podemos utilizar:</p> 
				<p>- una cebolla</p>
				<p>- una zanahoria</p>
				<p>- un puerro</p>
				<p>- una rama de apio.</p>
	</div>

<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>En una cazuela derretimos la mantequilla y tostamos la harina hasta que torne oscura. En el caso de que queramos meter los ingredientes opcionales derretimos la mantequilla y pochamos las verduras. Después añadimos la harina y dejamos que tome color.</p>
			<p>Por último añadimos el fondo oscuro y dejamos cocer hasta que ligue y empiece a espesar. Pasamos por el chino y servimos nuestra salsa con el plato que hayamos elegido.</p>
	</div>
</main>