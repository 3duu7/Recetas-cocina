<h1>Sangria</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 8 personas</p></strong>
				<p>Vino tinto 1 l</p>
				<p>Melocotón 2</p>
				<p>Naranjas 2</p>
				<p>Limón 1</p>
				<p>Plátano 2</p>
				<p>Manzanas 1</p>
				<p>Azúcar 60 g</p>
				<p>Refresco de naranja 330 ml</p>
	</div>
	
<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Para hacer esta bebida típica veraniega, comenzamos disolviendo el azúcar en 75 ml de agua caliente para formar un almíbar y lo que facilitará su disolución en el vino. En un recipiente amplio o un bol grande, ponemos el vino y el almíbar y añadimos el zumo de una naranja removiendo para que se mezcle bien.</p>

			<p>Vamos pelando las frutas y cortándolas en trozos pequeños, lo que facilitará su posterior maceración. Hacemos esa operación con todas a excepción de la otra naranja que no pelamos sino que cortamos en gajos o medias rodajas. Echamos todos los pedazos de fruta al bol con el vino y añadimos el zumo de un limón y un palo de canela, dejando que repose entre dos y tres horas para su maceración.</p>

			<p>Una vez haya macerado nuestra pócima, se vuelca a una jarra transparente y se sirve la sangría acompañada de unos cubos de hielo, adornando si se quiere con una larga peladura de naranja como adorno.</p>

			<p>Si se desea se puede añadir una bebida carbónica tipo gaseosa azucarada o un refresco de naranja o de limón, aunque conviene añadirlos en el momento de servir para que conserven su gas y no al principio porque ralentiza la maceración. Normalmente se sirve en vasos o copas amplias con alguna fruta que también se puede comer.</p>
	</div>
</main>