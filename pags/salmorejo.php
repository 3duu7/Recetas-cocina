		<h2>Elaboracion</h2>
			 <p>Comienza lavando los tomates, retirando lo verde del pedúnculo y triturándolos. No es necesario pelar ni quitar las pepitas porque después paso el puré de tomate por un colador fino donde se queda todo pasando solamente el tomate.</p>

			<p>En un bol coloca el pan y lo cubro con el puré de tomate dejando que se impregne durante unos diez minutos. Pasado ese tiempo, incorpora el diente de ajo y trituro bien con la batidora o con la Thermomix y obtenga una crema espesa de pan y tomate.</p> 

			<p>A continuación incorpora el aceite de oliva virgen extra.</p>

			<p>Tras echar el aceite volvemos a turbinar todo en el robot de cocina o a base de batidora y paciencia hasta que nuestro salmorejo sea uniforme, con un bonito color anaranjado y suficientemente compacto como para aguantar sobre su superficie los tradicionales tropezones de guarnición con los que se decora cada ración.</p>
