	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Empezamos pelando las patatas y cortándolas largas. Las ponemos en un bol con abundante agua para quitarles el almidón, de este modo no se pegarán al freírlas, las escurrimos y las secamos bien con un paño limpio de cocina.

			<p>Echamos aceite de oliva en una sartén a fuego medio, cuando esté lo suficientemente caliente, añadimos todas las patatas y las dejamos hacer hasta que estén tiernas.</p>
			<p>Subimos el fuego al máximo y las doramos ligeramente, no demasiado porque si quedan muy crujientes no absorberán bien el huevo. Sacarlas con una espumadera y ponerlas en una bandeja.</p>

			<p>En esa misma sartén, quitamos aceite hasta dejar solo unas pocas cucharadas. Subimos el fuego al máximo. Echamos los huevos y los dejamos durante un minutos que cuaje la clara.</p> 
			<p>Con mucho cuidado y ayudándonos de una espumadera, les damos la vuelta y los ponemos sobre las patatas. Ahora solo queda romperlos con la ayuda de un tenedor y una cuchara. Servirlos inmediatamente.</p>

