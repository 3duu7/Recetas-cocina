<h1>Pesto</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<p>Albahaca fresca, sólo las hojas 100 g</p>
			<p>Queso Parmesano 200 g</p>
			<p>Piñones 75 g</p>
			<p>Diente de ajo 2</p>
			<p>Aceite de oliva virgen extra 160 ml</p>
			<p>Sal</p>
	</div>
	
<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Separamos las hojas de albahaca del tallo (no lo utilizamos porque amarga y estropea el resultado), las lavamos y secamos a conciencia. Para ello, las extendemos sobre una hoja de papel absorbente de cocina, colocamos otra hoja encima y presionamos ligeramente con la palma de la mano, con cuidado de no romper ninguna hoja.</p>

			<p>Eliminar toda el agua es un paso imporantante, así que hay que tomárselo con calma. La albahaca es una planta muy delicada que tiene a oxidarse con facilidad. Cualquier gota de agua o cualquier desgarro de las hojas producirá un cambio en el color de nuestro pesto, volviéndolo oscuro en lugar de quedar verde brillante y claro.</p>

			<p>Mientras las hojas de albahaca se secan, pelamos los dientes de ajo, los cortamos en dos, a lo largo, y retiramos el gérmen. Tostamos los piñones en una sartén, sin aceite. Colocamos todos los ingredientes en el vaso de un robot de cocina, junto con la mitad del aceite y una pizca de sal, y trituramos.</p>

			<p>Cuando hayamos obtenido una papilla y no se aprecien trozos de piñones ni de albahaca, dejamos de triturar para añadir el resto del aceite. Trituramos de nuevo, sólo para integrar el aceite, y ya tenemos nuestro pesto de albahaca listo para utilizar.</p>
	</div>
</main>