<h1>Bechamel</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			 	<p>1 litro de leche entera</p>
			 	<p>60-80 gr de mantequilla</p> 
			 	<p>60-80 gr de harina</p> 
			 	<p>sal</p>
			 	<p>pimienta</p>
			 	<p>nuez moscada</p>
	</div>
	
<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Derretimos la mantequilla, mezclamos con la harina y removemos durante un minuto a fuego medio. Después apartamos y dejamos enfriar el roux. Cuando la leche esté hirviendo la echamos en el roux poco a poco mezclando y disolviendo todos los posibles grumos. </p>
			
			<p>Cuando hayamos terminado volvemos a poner sobre el fuego. Dejamos cocer, removiendo de vez en cuando, unos 10 minutos. Al final añadimos la sal, la pimienta negra recién molida y la nuez moscada.</p>
	</div>
</main>


