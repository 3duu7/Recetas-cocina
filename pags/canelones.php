		<h2>Ingredientes</h2>
			<strong><p>Para 6 personas</p></strong>
				<p>Carne picada de vacuno y<br> cerdo 500 g</p>
				<p>Cebolla 1</p>
				<p>Dientes de ajo 2</p>
				<p>Paté de hígado de cerdo 125 g</p>
				<p>Tomate frito casero 200 ml</p>
				<p>Láminas de pasta</p>
				<p>Aceite de oliva virgen extra</p>
				<p>Sal</p>
				<p>Harina de trigo 80 g</p>
				<p>Mantequilla 100 g</p>
				<p>Leche 1 l</p>
				<p>Queso rallado para gratinar 200 g</p>
	</div>
	
