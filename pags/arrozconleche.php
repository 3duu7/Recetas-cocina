<h1>Arroz con leche</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 4-6 personas</p></strong>
				<p>200 gr de arroz redondo</p>
				<p>1 litro de leche.</p>
				<p>100 gr de azúcar.</p>
				<p>2 ramas de canela.</p>
				<p>La cáscara de un limón.</p>
	</div>

<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Primero vamos a aromatizar la leche en la que coceremos el arroz. Para ello echa en una olla la leche junto con el azúcar, las ramas de canela y la cáscara de limón, y ponla a fuego medio hasta que hierva, es decir, hasta que veas que empieza a burbujear.</p>

			<p>Mientras, recurre a este truco para que el arroz no se te pegue y es eliminando gran parte de su almidón. Pon el arroz en un colador bajo un chorro de agua fría, remuévelo con tus propias manos y tenlo así durante al menos un par de minutos para que suelte todo ese almidón. Esto no va a afectar a la cremosidad del arroz, pero así es más difícil que se te pegue.</p>

			<p>Cuando la leche esté hirviendo añade el arroz y baje el fuego a temperatura suave para que se cueza lentamente durante 45-50 minutos. Ve removiéndolo de vez en cuando (yo suelo hacerlo cada 5 minutos más o menos).</p>

			<p>Cuando esté casi listo tendrás que remover con más frecuencia para que no se pegue al fondo y vigilar la cantidad de leche líquida que queda en la olla, ya que hay a quien le gusta más caldoso o bien más espeso, por lo que el punto perfecto lo decides tú..</p>

			<p>Antes de que se enfríe quita las ramas de canela y la cáscara de limón y échalo en los recipientes en los que vayas a servirlo (con esta receta tendrás unas 6 raciones como las que puedes ver en la fotografía), para que termine de asentarse.</p>

			<p>Espolvorea con canela por encima o bien carameliza su superficie.</p>
	</div>
</main>



	
