<h1>Salsa de tomate</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<p>2 kg de tomate maduro</p>
			<p>3 zanahorias</p>
			<p>2 pimientos verdes</p>
			<p>1 cebolla grande</p>
			<p>aceite de oliva virgen extra</p>
			<p>sal</p>
			<p>azúcar</p>
	</div>
	
<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			 <p>Lavamos y secamos los tomates. Les sacamos alguna parte, si es necesario o está fea, y los troceamos. En una cazuela ponemos un poco de aceite de oliva a fuego medio. Echamos la cebolla y los pimientos cortados en juliana, y las zanahorias en rodajas finas.</p> 

			 <p>Rehogamos todo hasta que la cebolla coja color marroncito, así la salsa tendrá más sabor. Añadimos el tomate troceado y dejamos a fuego lento, removiendo de vez en cuando, durante una hora. </p>

			 <p>Pasamos la salsa por el pasapurés y volvemos a poner al fuego. La dejaremos durante otra hora más, teniendo cuidado de que no se pegue.</p>

			 <p>Salamos y echamos una pizca de azúcar si nos resulta muy ácida. Apagamos el fuego, rellenamos los botes con la salsa de tomate y dejamos destapados hasta que se enfríen totalmente.</p>

			 <p>Si queremos congelar los botes dejaremos un espacio de dos centímetros hasta el borde, ya que al congelarse el líquido aumentará su volumen y de este modo evitamos que el frasco reviente</p>
	</div>
</main>