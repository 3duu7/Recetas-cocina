<h1>Fabada asturiana</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 4 personas:</p></strong>
			<p>Fabes de La Granja 450 g</p>
			<p>Cebolla 250 g</p>
			<p>Dientes de ajo 2</p>
			<p>Chorizo asturiano 2</p>
			<p>Lacón salado 200 g</p>
			<p>Tocino 200 g</p>
			<p>Laurel hoja 1</p>
			<p>Aceite de oliva virgen extra 45 g</p>
	</div>
	
<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>La noche anterior comenzaremos dejando las habas en remojo en un recipiente grande, por otra parte en otro bol pondremos a desalar nuestro lacón.</p>

			<p>Al día siguiente echamos las habas en una olla de fondo grueso, añadiremos los dientes de ajo, la cebolla entera pelada, el aceite de oliva y la hoja de laurel, cubriremos con agua, mejor mineral, hasta dos dedos por encima de nuestras fabes, dejando que lleguen a la ebullición y cortando la cocción con un chorro de agua fría, operación que repetiréis a lo largo de la cocción otras dos veces más.</p>

			<p>Añadiréis toda la carne a la olla donde se cuecen las habas una vez que las hayamos asustado.</p>

			<p>Seguir cociendo a fuego medio-bajo durante tres horas aproximadamente, desespumando si es necesario y observando que no se quede seca, en el caso de ser así, vais añadiéndole pequeñas cantidades de agua caliente, bien sola o bien de la olla donde en un principio habíamos escaldado las carnes, acordándose otras dos veces de asustar la fabada con agua fría a lo largo del tiempo de cocción.</p>

			<p>Justo cuando las fabes ya están cocidas y ha pasado el tiempo, comprobar la sal y añadirle la que necesite, dejando cocer la fabada unos minutos para que la sal se incorpore. No echarla al principio sino en este momento.</p>
	</div>
</main>