<h1>Lubina al horno</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 4 personas</p></strong>
				<p>Lubina 1.2kg</p>
				<p>Patata 1</p>
				<p>Cebolla 1</p>
				<p>Aceite de oliva virgen extra</p>
				<p>Agua 75 ml</p>		
	</div>

<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Comenzamos cortando las patatas con una mandolina. Con una grande o dos de buen tamaño podréis cubrir la bandeja del horno. Después picamos la cebolla en juliana fina, y la repartimos sobre las patatas. Añadimos un poco de sal, un chorrito de aceite de oliva virgen extra, 75 ml de agua y horneamos durante quince minutos a 180º.</p>

			<p>Salpimentamos el pescado y pincelamos con una pizca de aceite y lo metemos en el horno sobre las patatas que ya estarán empezando a cocinarse.</p>

			<p>Dejamos que el pescado se ase a la misma temperatura hasta que metiendo una puntilla por debajo de la raspa, veamos que se levanta con facilidad y entonces apagamos el horno y sacamos la fuente. El tiempo necesario estará entre los 12 y los 20 minutos dependiendo del tamaño de la pieza.</p>

			<p>Retiramos el pescado de la fuente del horno, quitamos la raspa y lo ponemos en una fuente para servirlo con su guarnición. Si las patatas no estuvieran o si vemos que es necesario que se doren un poco más, metemos las patatas otros cinco minutos al horno.</p>
	</div>
</main>