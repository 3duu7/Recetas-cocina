<h1>Salsa barbacoa</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 4 personas</p></strong>
				<p>Cebolleta 1</p>
				<p>Azúcar moreno 15 g</p>
				<p>Dientes de ajo 1</p>
				<p>Ketchup 30 g</p>
				<p>Tomate concentrado 15 g</p>
				<p>Salsa Worcestershire 15 g</p>
				<p>Miel 30 g</p>
				<p>Pimentón dulce 10 g</p>
				<p>Aceite de oliva virgen extra 15 ml</p>
				<p>Sal y pimienta al gusto</p>
	</div>
	
<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Comenzaremos colocando al fuego una sartén con el aceite de oliva, añadimos la cebolleta picada menuda y el diente de ajo con el azúcar moreno. Lo sofreímos todo durante unos minutos hasta que comience a caramelizar. Salpimentamos. Retiramos del fuego.</p>

			<p>Añadimos el ketchup, el concentrado de tomate, la salsa Worcestershire, la miel y el pimentón. Mezclamos y trituramos con una batidora.</p>
	</div>
</main>