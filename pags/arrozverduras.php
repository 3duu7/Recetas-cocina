<h1>Arroz con verduras</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 2 personas</p></strong>
				<p>1/2 taza de arroz 100 g</p>
				<p>Aceite de oliva virgen extra</p>
				<p>4 dientes de ajo lamin</p>
				<p>1/2 pimiento rojo en tiras</p>
				<p>1/2 taza de judías verdes 70 g</p>
				<p>1/2 taza de habas 70 g</p>
				<p>1/2 taza de alcachofas 70 g</p>
				<p>1 tomate troceado</p>
				<p>1 cucharadita de pimentón dulce</p>
				<p>2 cucharaditas de romero seco</p>
				<p>2 cucharaditas de tomillo seco</p>
				<p>1 cucharadita de cúrcuma en polvo</p>
				<p>Pimienta negra molida al gusto</p>
				<p>Limón (opcional)</p>	
	</div>
	
<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Cocina el arroz siguiendo las instrucciones del paquete (yo usé 1 taza de agua ó 250 ml y estuvo listo en 25 minutos, aunque el agua y el tiempo de cocción varían en función de la marca).</p>

			<p>En una paellera, sartén profunda u olla echa el aceite y cuando esté caliente echa los dientes de ajo y el pimiento. Cocina a fuego medio alto unos 5 minutos, removiendo de vez en cuando.</p>

			<p>Echa las judías, las habas y las alcachofas. Cocina durante unos 10 minutos más.
			Añade el tomate (yo no le quito la piel) y cocina durante 10 minutos.</p>

			<p>Por último, añade el arroz integral colado, el resto de los ingredientes (menos el limón), remueve y cocina durante 5 minutos para que se mezclen los sabores.</p>

	</div>
</main>