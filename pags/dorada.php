<h1>Dorada al horno</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 2 personas</p></strong>
				<p>Dorada 500 g</p>
				<p>Patatas 2</p>
				<p>Cebollitas francesas 10</p>
				<p>Dientes de ajo 1</p>
				<p>Zumo de limón 1</p>
				<p>Guindilla 1</p>
				<p>Sal al gusto</p>
				<p>Aceite de oliva virgen extra al gusto</p>
	</div>

<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Calentamos abundante aceite en una sartén. Pelamos y cortamos las patatas en rodajas panadera, las freímos ligeramente sin que lleguen a dorarse y retiramos a un plato con papel absorbente. Bajamos el fuego, que esté muy bajito y hacemos las cebollitas francesas, casi como si se confitasen, durante 15 minutos.</p>

			<p>Escurrimos y retiramos también a un plato. Calentamos el horno a 180º C con calor arriba abajo sin aire. En una fuente para horno ponemos como base las patatas y las cebollitas y sobre éstas la dorada abierta, que habremos salado previamente.</p>

			<p>Regamos con un poquito de aceite de oliva y horneamos durante unos 20 minutos o hasta que la dorada esté lista, dependerá del tamaño. Aparte en una sartén doramos un diente de ajo laminado con la guindilla, retiramos y vertemos el zumo de limón.</p>

			<p>En cuanto saquemos la dorada del horno regamos con este refrito y servimos inmediatamente. El refrito debe estar bien caliente para que no enfríe el pescado, así que hacedlo más o menos a la vez, cuando la dorada ya salga del horno.</p>
	</div>
</main>