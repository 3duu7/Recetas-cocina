<h1>Arroz con acelgas</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 2 personas</p></strong>
				<p>Un manojo de acelgas pequeño</p>
				<p>1 cebolla o cebolleta pequeña</p>
				<p>4 dientes de ajo </p>
				<p>1 tomate maduro</p>
				<p>1 hoja de laurel</p>
				<p>Una punta de cucharilla de <br>postre de pimentón de la vera</p>
				<p>1 pastilla de caldo de pollo</p>
				<p>1 l de agua 100 g de arroz</p>
				<p>Unas hebras de azafrán</p>
	</div>
	
<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Lavamos las acelgas y las troceamos, picamos la cebolla y rallamos el tomate.</p>

			<p>En una sartén con aceite de oliva virgen sofreímos la cebolla y cuando empiece a verse transparente añadimos el tomate rallado y las acelgas troceadas, también los dientes de ajo.</p>

			<p>Hacemos a fuego bajo durante unos 10 minutos y en el último momento añadimos el pimentón y la pastilla de caldo de pollo,  removemos y echamos en una cacerola con el litro de agua, lo tendremos cociendo durante unos 15 minutos, el caldo habrá reducido, añadimos el arroz, una hoja de laurel y unas hebras de azafrán tostado.</p>

			<p>Coceremos durante aproximadamente 20 minutos, rectificaremos de sal si fuera necesario, este arroz se toma caldoso, pero se suele dejar reposar unos 5 minutos para que embeba un poquito el caldo.</p>
	</div>
</main>

	
	

