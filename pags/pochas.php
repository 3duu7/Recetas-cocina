<h1>Pochas con almejas</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 6 personas:</p></strong>
				<p>1.300 gr. de pochas</p> 
				<p>1 cabeza de ajos</p>
				<p>1 cebolla</p>
				<p>2 tomates</p>
				<p>1 pimiento rojo</p>
				<p>agua</p>
				<p>aceite virgen extra</p>
				<p>sal</p>
				<p>1 cucharita de pimentón picante</p>
	</div>

<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
		<p>Limpia las pochas y escúrrelas. Ponlas a cocer en una cazuela con abundante agua. Añade la cabeza de ajos entera (sin pelar), la cebolla y los tres tomates. Sazona y cuece lentamente a fuego suave durante 35 minutos. </p>

		<p>Retira las verduras de la cazuela y pásalas por el pasa purés.</p>

		<p>Añade el puré resultante a la cazuela y mezcla suavemente.</p>

		<p>Deja cocer todo junto durante 5-10 minutos.</p>

		<p>Pica el pimiento rojo en tacos y ponlos a dorar en una sartén con un poco de aceite. Cuando esté a punto, retira la sartén del fuego, deja templar un poco y añade el pimentón. Mezcla bien e incorpora a las pochas. Sirve en una legumbrera.</p>
	</div>
</main>