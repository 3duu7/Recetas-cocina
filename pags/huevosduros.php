<h1>Huevos duros con verduras</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 4 personas</p></strong>
				<p>10 huevos<p>
				<p>250 gr. carne picada<p>
				<p>1/2 cebolla<p>
				<p>150 ml. vino blanco<p>
				<p>Salsa de tomate<p>
				<p>Queso rallado para gratinar<p>
				<p>Sal<p>
				<p>Aceite oliva<p>
			<strong><p>Para hacer la bechamel:<p></strong>
				<p>1/2 litro leche<p>
				<p>2 cucharadas harina<p>
				<p>15 gr. mantequilla<p>
				<p>sal<p>
				<p>Nuez moscada<p>
	</div>

<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>En una cacerola ponemos agua con sal, cuando este hirviendo incorporamos los huevos, los tendremos cociendo 20 minutos pasados el tiempo escurrimos y dejamos enfriar.</p>

			<p>En una sartén ponemos aceite, echamos la carne y empezamos a freírla a fuego medio, salamos.Cuando empiece a tomar color, ponemos la cebolla y el ajo y dejamos que todo se haga bien.</p>

			<p>Una vez este todo bien frito incorporamos el vaso de vino y esperaremos a que se reduzca lentamente. Pelamos los huevos cocidos, los partimos por la mitad y separamos las yemas de las claras.</p>

			<strong><p>Hacemos la bechamel</p></strong>

			<p>Colocamos la carne en un bol y rallamos todas las yemas. Incorporamos muy bien las yemas con la carne.</p>

			<p>Añadimos unas cucharadas de bechamel a la carne y removemos.Echaremos la bechamel que necesiten hasta que veamos que es una masa jugosa. Una vez echa nuestra mezcla, vamos rellenando los huevos. Cuando estén todos los huevos con su relleno, los cubrimos con la bechamel.</p>

			<p>Encima de cada huevo vamos a poner una cucharadita de tomate frito (esto le dará un color muy bonito al gratinado). Echamos por encima los quesos rallado. Los metemos en el horno en la bandeja central y los ponemos a gratinar unos diez minutos.</p>
	</div>
