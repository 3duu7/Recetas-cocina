<h1>Rape con salsa verde</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 2 personas</p></strong>
				<p>1 o 2 rodajas de rape</p>
				<p>250 gr de almejas</p>
				<p>2 dientes de ajo</p>
				<p>1 cebolla grande</p>
				<p>1 cucharada sopera de harina.</p>
				<p>100 ml de vino blanco</p>
				<p>500 ml de caldo de pescado</p>
				<p>Aceite de oliva virgen</p>
				<p>Sal</p>
	</div>

