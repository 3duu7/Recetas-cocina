	<div class="caja3">
		<h2>Elaboracion</h2>
		<p>Lava las almejas bajo un chorro de agua fría y déjalas sumergidas en un bol con agua fría y una cucharada sopera de sal gruesa durante al menos 2 horas.Al cabo de ese tiempo, escúrrelas y lávalas de nuevo muy bien para quitar la sal y los posibles restos de arena, y retira las que ya estén abiertas.</p>

		<p>En la cazuela en la que vayas a preparar la receta, echa un poco de aceite y las almejas, tapa, pon el fuego a media temperatura y espera hasta que las almejas se abran, normalmente tardan entre 3 y 4 minutos. Cuando prácticamente estén todas abiertas, sácalas de la cazuela y resérvalas en un bol o plato hondo junto con el caldo que ha salido de ellas. Si alguna sigue cerrada, deséchala.</p>

		<p>Pela la cebolla y pícala muy finita con el cuchillo. Pela los ajos y pícalos bien finitos.En la cazuela que ya habíamos utilizado con las almejas pon un poco de aceite y a fuego medio-bajo saltea el ajo durante unos segundos y a continuación añade la cebolla y un poco de sal. Deja que se cocine poco a poco hasta estar transparente, sin apenas dorarse (tardará casi 10 minutos).</p>

		<p>Añade la harina y revuélvelo todo durante un par de minutos para que la harina se cocine un poco y no sepa cruda en la salsa.
		Sube el fuego para que esté a temperatura alta, echa el vino blanco y deja que se evapore el alcohol durante un par de minutos.</p>

		<p>Salpimenta las rodajas de rape por ambos lados. Añade el caldo de pescado y el caldo de las almejas colado, y en cuanto empiece a hervir incorpora las rodajas de rape. Baja el fuego para que esté suave, tapa la cazuela y deja que se cocine todo junto 5 minutos.</p>

		<p>Lava el perejil, sécalo con papel de cocina, quítale los tallos y pica muy finas las hojas. Échalo a la sartén junto con las almejas, tapa la cazuela de nuevo y cocina 2 minutos más.</p>
