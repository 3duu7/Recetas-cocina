<h1>Smoothie</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>2 vasos grandes</p></strong>
				<p>2 tazas de fresas congeladas</p>
				<p>1 plátano pelado y troceado</p>
				<p>1 taza de leche</p>
				<p>1 taza de cubitos de hielo</p>
				<p>1 cucharada de miel (opcional)</p>
	</div>
	
<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Añade al vaso de la batidora el helado, el plátano en trozos, la leche, el hielo y la miel, y bátelo durante unos segundos a la máxima velocidad, hasta que se pulvericen todos los ingredientes.</p>

			<p>Si ves que la consistencia es muy densa, vierte más leche y bate unos segundos más. Si está muy líquida, incorpora más fruta y hielo y sigue batiendo.</p>
	</div>

</main>