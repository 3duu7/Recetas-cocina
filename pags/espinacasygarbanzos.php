<h1>Garbanzos con espinacas</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 4 personas:</p></strong>
			<p>Garbanzos cocidos 500 g</p>
			<p>Espinaca congelada 400 g</p>
			<p>Dientes de ajo 2</p>
			<p>Pan de hogaza rebanada 1</p>
			<p>Comino molido 5 g</p>
			<p>Cilantro molido 5 g</p>
			<p>Pimentón dulce 5 g</p>
			<p>Pimienta blanca molida pizca</p>
			<p>Salsa de tomate 60 g</p>
			<p>Vinagre de manzana 30 g</p>
			<p>Agua 100 ml</p>
			<p>Aceite de oliva virgen extra 100 g</p>
			<p>Sal 100 g</p>
	</div>
	
<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>En una cazuela con un par de dedos de agua ponemos a cocer a fuego medio las espinacas congeladas durante unos 15 minutos o el tiempo necesario para que se descongelen. <p><p>Tendremos cuidado con el agua para que no se consuma, pues no queremos que se nos agarren a la cazuela. Una vez listas, las escurrimos bien de agua y las picamos con un cuchillo afilado.<p>

			<p>En una sartén calentamos el aceite y freímos los dientes de ajo previamente pelados. Cuando comiencen a dorarse los retiramos y los colocamos en el vaso de una batidora de mano.En el mismo aceite freímos la rebanada de pan hasta dorar.</p>

			<p>La escurrimos y la añadimos al vaso de la batidora junto con el comino, el cilantro, el pimentón, la pimienta blanca, la salsa de tomate y un pellizco de sal. Añadimos también al vaso el vinagre, el agua y la mitad del aceite. Trituramos bien y reservamos.</p>

			<p>En una cazuela o sartén grande vertemos el aceite que nos ha sobrado de freír el ajo y el pan, lo calentamos y añadimos las espinacas picadas.</p> 

			<p>Salteamos durante un par de minutos antes de agregar la majada que tenemos reservada y cocemos dos minutos más, removiendo para que se mezclen los sabores.</p>

			<p>Por último añadimos los garbanzos cocidos y salteamos durante cinco minutos. Probamos de sal y rectificamos si fuera necesario antes de servir.</p>	
	</div>
</main>
