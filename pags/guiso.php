<h1>Gusio de ternera con verduras</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 4personas</p></strong>
				<p>300 gramos de carne de ternera</p>
				<p>1 unidad de Pimiento verde</p>
				<p> 1 unidad de cebolla</p>
				<p>2 cucharadas soperas de tomate</p>
				<p>1 unidad de Ajo</p>
				<p>1 rama de Orégano fresco</p>
				<p>1 unidad de zanahoria</p>
				<p> 1 unidad de patata</p>
				<p>50 gramos de guisantes</p>
				<p>50 gramos de judías verdes</p>
				<p>150 mililitros de Vino blanco</p>
				<p>250 mililitros de Agua</p>
				<p>1 pizca de Sal</p>
				<p>1 chorro de Aceite de oliva</p>		
	</div>
	
<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Comenzamos esta carne guisada sellando la carne en una olla con 4 cucharadas de aceite de oliva: ponemos la olla a fuego alto, salpimentamos la carne y la doramos hasta que se selle por todos lados, unos 2 minutos. Sacamos y retiramos.</p>

			<p>En la misma olla, agregamos la cebolla, el ajo y el pimiento. Hemos cortado la cebolla y el pimiento en trozos medianos. En cambio, el ajo lo dejamos entero. Dejamos cocinar durante 3 minutos.</p>

			<p>Añadimos la zanahoria pelada y cortada en rodajas, junto con los tomates cherrys cortados por la mitad. Ponemos sal y cocinamos otros 2 minutos.</p>

			<p>Hemos utilizado tomate cherry porque lo teníamos por casa, pero también puedes sustituirlos por un tomate normal.</p>

			<p>Para continuar con este plato de ternera guisada, volvemos a incorporar la carne de res a la olla. Movemos bien para que se mezclen los ingredientes.</p>

			<p>Agregamos las hierbas aromáticas y el tomate concentrado a nuestro guiso de carne con papas. Movemos, dejamos que se integre bien el tomate y que se cocine durante 2 minutos.</p>

			<p>A continuación, agregamos el vino blanco, dejamos que se evapore el alcohol durante 3 minutos y agregamos el agua. Dejamos que el guiso de ternera cueza durante 30 minutos.</p>

			<p>Es el momento de añadir las patatas peladas y cascadas en gajos. Ponemos un pelín más de sal y dejamos cocer durante 20 minutos más.</p>

			<p>Por último, incorporamos los guisantes y las judías congeladas a la carne con papas y dejamos que se cocinen durante 10 minutos.</p>
	</div>
</main>