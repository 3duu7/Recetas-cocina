<h1>Espaqguetis con albondigas</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 4 personas</p></strong>
				<p>1 paquete de espaguetis</p>
				<p>Salsa de tomate casera</p>
				<p>1 taza de pan rallado</p>
				<p>1/3 taza de leche</p>
				<p>250 g de carne picada de ternera</p>
				<p>250 g de carne picada de cerdo</p>
				<p>1 taza de queso parmesano rallado</p>
				<p>1 cucharadita de sal</p>
				<p>1/3 taza de perejil picado</p>
				<p>1/2 cucharadita de nuez moscada molida</p>
				<p>1/4 cucharadita de pimienta molida</p>
				<p>2 huevos (batidos)</p>
				<p>2 dientes de ajo</p>
	</div>

<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Mezclar la leche y el pan rallado en un tazón pequeño. Dejar apartado.</p>

			<p>Colocar la carne de cerdo y la de ternera en un bol grande y agregar el queso, la sal, la pimienta, el perejil, el ajo, la nuez moscada y los huevos batidos.</p>

			<p>Escurrir la leche del pan rallado y agregar a la mezcla de carne.</p>

			<p>Mezclar todos los ingredientes suavemente con la mano. Intentar no comprimir o presionar la mezcla de carne. Se trata únicamente de mezclarlos bien.</p>

			<p>Refrigerar 20 minutos para que sea más fácil dar forma a las albóndigas. Mientras preparamos la salsa de tomate casera.</p>

			<p>Una vez pasados los 20 minutos en el frigorífico sacamos la mezcla de carne picada y vamos a formar las albóndigas del tamaño de una pelota de golf con un toque suave. Añadir directamente a la salsa de tomate casera 30 minutos antes de que la salsa se termine de hacer.</p>

			<p>Mientras vamos cociendo los espagueti según las instrucciones del paquete. Mezclamos la salsa de tomate con albóndigas con los espagueti y espolvoreamos con queso rallado.</p>

			<p>Estos espagueti con albóndigas están sencillamente deliciosos y es un completo y nutritivo plato que nuestros hijos adorarán…</p>
	</div>
</main>