<h1>Bacalao con sanfaina</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 2 personas</p></strong>
				<p>Bacalao desalado 800 g</p>
				<p>Harina de trigo 30 g</p>
				<p>Cebolla 1</p>
				<p>Diente de ajo 1</p>
				<p>Pimiento rojo 1</p>
				<p>Pimiento verde italiano 1</p>
				<p>Calabacín 1</p>
				<p>Berenjena 1</p>
				<p>Tomate en rama 6</p>
				<p>Tomate concentrado 10 ml</p>
				<p>Vino blanco 100 ml</p>
				<p>Pimienta negra molida</p>
				<p>Sal</p>
				<p>Perejil o tomillo fresco (opcional)</p>
				<p>Aceite de oliva virgen extra</p>
	</div>
	
<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Si usamos bacalao salado, habrá que empezar desalándolo al menos 48 horas antes de preparar esta receta. Una vez listo, cortar en piezas de ración o usar directamente 4 lomos de unos 200 g cada uno. Reservar.</p>

			<p>Pelar la cebolla y el diente de ajo. Lavar y secar el resto de verduras. Cortar en cubos la cebolla picar el diente de ajo y cortar el resto, sin pelar, en cubos de unos 2-3 cm, desechando las semillas de los pimientos. Dorar a fuego medio-alto la berenjena en una sartén antiadherente sin añadir nada más, hasta que coja color (no es un paso necesario pero sí recomendable). Reservar.</p>

			<p>Calentar un par de cucharadas de aceite de oliva virgen extra en una sartén alta o cazuela ancha y añadir la cebolla. Salar, saltear un par de minutos, añadir el ajo y dejar pochar a fuego suave, para que se liberen sus azúcares de forma natural. Cuando la cebolla empiece a transparentar incorporar los pimientos y cocinar el conjunto unos 10 minutos.</p>

			<p>Añadir el calabacín, cocinar 1 minuto y echar también la berenjena. Salpimentar, dar unas vueltas y regar con el vino. Cuando se evapore el alcohol, agregar el tomate rallado o pelado y troceado, o usar tomate en conserva. Añadir también el concentrado de tomate, remover y dar otro ligero punto de sal. Bajar el fuego, tapar y dejar cocer unos 35-40 minutos, removiendo de vez en cuando.</p>

			<p>Secar el bacalao muy bien con papel de cocina y enharinar ligeramente. Dorar por ambos lados lo justo para que se marque, pero sin cocinarlo del todo. Retirar y añadir a la cazuela de las verduras cuando estén ya en su punto. Tapar y dejar cocinar unos 5 minutos, hasta que el bacalao se haya cocinado del todo y se separe fácilmente en lascas. Servir con perejil o tomillo.</p>
	</div>
