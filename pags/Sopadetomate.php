<h1>Sopa de tomate</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 4 personas</p></strong>
				<p>800 gr de tomates maduros.</p>
				<p>2 dientes de ajo.</p>
				<p>1/2 cebolla mediana.</p>
				<p>200 ml de caldo de verduras casero o agua.</p>
				<p>1 zanahoria mediana.</p>
				<p>1 cucharada sopera de harina de trigo.</p>
				<p>4 hojas de albahaca fresca.</p>
				<p>1/2 cucharadita de postre de azúcar(opcional).</p>
				<p>Aceite de oliva</p> 
				<p>sal</p>
				<p>pimienta negra recién molida.</p>	
	</div>

<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Pela y pica muy finita la cebolla y haz lo mismo con los dientes de ajo.Pela la zanahoria y córtala también en trocitos pequeños.</p>

			<p>En una olla pon un poco de aceite y sal y echa la cebolla, los ajos y la zanahoria. Deja que se cocine todo junto a fuego medio removiendo de vez en cuando durante unos 5 minutos hasta que se empiecen a dorar. Si se pegan o doran las verduras, baja la temperatura del fuego y échales un poquito de agua.</p>

			<p>Incorpora la harina y mézclala con las verduras durante 1 minuto. Esto ayudará a que la sopa después espese un poco más y cocinando la harina no sabrá cruda.Los tomates deben utilizarse sin piel así que puedes optar por varios métodos. </p>
			
			<p>Incorpora los tomates a la olla junto con 4 o 5 hojas de albahaca picadas, un poco de sal, el azúcar y el caldo de verduras.Sube el fuego para que esté alto y cuando la mezcla empiece a hervir baja el fuego para que esté suave y cocina 30 minutos.</p>

			<p>Una vez lista la sopa de tomate, tritúrala y ya la tienes lista. Pruébala por si tienes que rectificarla de sal, y si la quieres más espesa tan solo tienes que echarla de nuevo en el cazo y calentarla hasta que tenga la textura que buscas. Igualmente si ha espesado demasiado siempre puedes añadir un poco de agua para aligerarla.</p>
	</div>
