<h1>Paella</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 4 personas</p></strong>
				<p>400 gramos de Arroz Bomba</p>
				<p>1 Kg. de pollo troceado</p>
				<p>500 gramos de conejo troceado</p>
				<p>200 gramos de judías verdes</p>
				<p>100 gramos de garrofó fresco</p>
				<p>100 gramos de tomate troceado</p>
				<p>6 cucharadas de aceite de oliva</p>
				<p>Unas hebras de azafrán</p>
				<p>Pimentón rojo molido</p>
				<p>Una ramita de romero</p>
				<p>Sal</p>
	</div>
	
<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Vierte el aceite sobre la paella y enciende el fuego. Una vez el aceite esté caliente, añade el conejo y el pollo y sofríelos hasta que adquieran un color dorado. Recuerda darle vueltas a las piezas de carne para que se hagan uniformemente. A continuación echa las verduras y rehógalas. Ahora vierte el tomate rallado y sofríelo del mismo modo, removiendo constantemente.</p>

			<p>Ahora es el turno del pimentón. Añádelo y remuévelo apenas un instante, enseguida tienes que verter el agua hasta los remaches de la paella para evitar que se queme.
			Añade el azafrán, el romero y la sal. Una vez que el agua rompa a hervir, déjala unos 5 minutos en ebullición con fuego vivo. A continuación baja a fuego medio y déjala cocer entre 20 y 30 minutos.</p>

			<p>Aviva el fuego y añade el arroz. La forma tradicional de ponerlo es haciendo una cruz en el agua que está hirviendo y repartirlo de modo uniforme por la superficie del recipiente. Aprovecha ahora para probar y rectificar de sal.</p>

			<p>Manten el fuego fuerte durante unos 10 minutos y reduce el fuego a la mitad durante un tiempo parecido hasta que el arroz quede en su punto de cocción.</p>
	</div>
</main>