<h1>Fideuá</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 4 personas</p></strong>
				<p>Fideos gruesos 400 g</p>
				<p>Rape 200 g</p>
				<p>Sepia 2</p>
				<p>Colas de gambas 16</p>
				<p>Dientes de ajo 2</p>
				<p>Tomate pera maduro 4</p>
				<p>Pimentón dulce</p>
				<p>Azafrán en hebra</p>
				<p>Aceite de oliva virgen extra</p>
				<p>Caldo de pescado o fumet1 l</p>		
	</div>

<main>
	<div class="caja3">
		<h2>Elaboracion</h2>

		<p>Troceamos el pescado en piezas de tamaño bocado. Calentamos un poco de aceite de oliva virgen extra en una paella o sartén y lo salteamos. Retiramos y reservamos. A continuación salteamos el marisco. Retiramos también y reservamos.</p>

		<p>En el mismo aceite, salteamos el ajo picado fino, y cuando se empiece a dorar añadimos el tomate pelado y rallado, el pimentón, las hebras de azafrán, los fideos y el caldo caliente. Salpimentamos al gusto y dejamos cocer a fuego medio unos siete u ocho minutos.</p>

		<p>En este punto añadimos el rape reservado y dejamos cocer un par de minutos más. Incorporamos el marisco pelado, tapamos y dejamos reposar dos minutos más, tiempo suficiente para que se cuezan las gambas con el calor residual. Si la fideuá queda seca durante la cocción, añadimos pequeñas cantidades de caldo de pescado, siempre caliente.</p>

		<p>Lo mejor es dejar reposo el guiso durante un par de minutos antes de servir, y tapado unos minutos para que los fideos absorban todo el caldo. Presentamos en la misma paella, de la que se servirán los comensales. La mejor guarnición para la fideuá tradicional es un poco de alioli, que debe mezclarse en el plato, y una copa de vino blanco bien fresco.</p>
