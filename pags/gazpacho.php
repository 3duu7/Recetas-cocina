<h1>Gazpacho</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 6 personas</p></strong>
				<p>Tomate pera 1 kg</p>
				<p>Pimiento verde italiano 1</p>
				<p>Pepino 1</p>
				<p>Dientes de ajo 2</p>
				<p>Aceite de oliva virgen extra 50 ml</p>
				<p>Pan de hogaza duro 50 g</p>
				<p>Agua 250 ml</p>
				<p>Sal l5 g</p>
				<p>Vinagre de Jerez 30 ml</p>
	</div>

<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Troceamos todos los ingredientes indicados en la proporción que os he puesto y añadimos 50 ml de aceite de oliva, 250 ml de agua de la nevera y 50 ml de vinagre de Jerez, triturando todo en la batidora de vaso o Turmix. No es necesario pelar los tomates o los pimientos porque luego lo vamos a pasar por el colador fino.</p>

			<p>Si tenéis un robot tipo Thermomix podéis poner todos los ingredientes en el vaso y triturarlos a máxima velocidad durante 4 minutos para obtener una textura perfecta.</p>

			<p>Una vez triturado, pasamos el gazpacho resultante por el colador fino, apretando con un cucharón para que quede una crema sin pieles ni semillas y lo metemos en la nevera un par de horas para que enfríe bien.</p>
	</div>
</main>