<h1>Huevos al plato</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 1 persona</p></strong>
				<p>Huevo, 1</p>
				<p>Puerro o cebolleta, 5 cm</p>
				<p>Salsa de tomate,  75 ml (*)</p>
				<p>Espárragos blancos en conserva, 2</p>
				<p>Chorizo, 25 g</p>
				<p>Jamón serrano o ibérico, 25 g</p>
				<p>Guisantes en conserva, 1/2</p>
				<p>Aceite de oliva virgen extra</p>
				<p>Sal</p>
	</div>

<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Troceamos el jamón y el chorizo en dados pequeños o en tiritas y el puerro o la cebolleta en rodajas muy finas. Ponemos todo en una cazuelita de barro o en cualquier otro recipiente apto para microondas, añadimos el aceite de oliva virgen extra (solo si el chorizo y el jamón no tienen apenas grasa, si la tienen con la que sueltan es suficiente y no hay que añadir más aceite).</p>

			<p>Cocinamos 1 minuto a 800 W de potencia.
			Reservamos las puntas de los espárragos y añadimos los tallos troceados junto con los guisantes escurridos y la salsa de tomate. Calentamos todo durante 1 minuto a 800 W.

			<p>Separamos la yema y la clara del huevo. Ponemos la clara del huevo en la cazuela en la que estamos preparando nuestros huevos al plato y calentamos en el microondas durante dos períodos de 30 segundos a 800 W (es decir, 30 segundos, paramos el microondas, abrimos la puerta para que entre aire y otros 30 segundos). Si el huevo fuese muy grande o estuviese muy frío de la nevera lo cocinaremos 15 segundos más.</p>
			
			<p>Ahora tendremos la clara cuajada por los bordes, pero cruda en el centro. Es el momento de añadir la yema que tenemos reservada. Cocinamos 30 segundos a 800 W y, al igual que antes, si el huevo es muy grande o está muy frío, seguimos calentando de 10 en 10 segundos hasta que veamos que la clara está casi cuajada (no importa que quede un pelín líquida porque con el calor residual termina de hacerse en lo que nos sentamos a la mesa).</p>
	</div>
</main>