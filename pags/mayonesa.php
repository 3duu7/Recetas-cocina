<h1>Salsa mayonesa</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<p>2 yemas de huevo</p>
			<p>300 ml de aceite de girasol</p>
			<p>sal</p>
			<p>zumo de limón</p>
	</div>

<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>En un bol ponemos las yemas con el zumo de limón y removemos. La cantidad de zumo de limón vendrá determinada por la cantidad total de mayonesa o mahonesa queramos. En este caso con una cucharada o poco más nos valdría.</p>

			<p>Agregamos el aceite poco a poco. Yo he usado aceite de girasol porque en mi opinión la mahonesa o mayonesa debe aportar suavidad al plato sin aportar un excesivo sabor. Algunas veces, si que le añado un poco de aceite de oliva virgen extra, pero siempre en mayor proporción el aceite de girasol.</p>

			<p>Cuando tengamos la salsa montada en su punto, ni excesivamente líquida ni excesivamente dura, le añadimos la sal, previamente disuelta con una cucharada de agua caliente. Mezclamos.
			Para evitar que se nos corte debemos intentar no cometer esos errores anteriores.</p>

			<p>Añadir el limón antes que el aceite tiene por objeto aumentar la salsa desde el principio por lo que no se cortará tan fácilmente.</p>
			<p>Una vez que se nos ha cortado la mayonesa se puede arreglar añadiendo en otro bol un par de cucharadas de agua caliente y echando poco a poco lo cortado, batiendo hasta el final.</p>
	</div>
</main>