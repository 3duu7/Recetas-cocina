	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 4 personas</p></strong>
				<p>Coñac100 ml</p>
				<p>Vino de Oporto100 ml</p>
				<p>Glas de carne40 g</p>
				<p>Nata líquida para cocinar400 ml</p>
				<p>Sal al gusto</p>
				<p>Granos de pimienta variados 20 g</p>
	</div>
	
<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Lo primero que debemos hacer es triturar los granos de pimientas variadas en un mortero y los esparcimos, vuelta y vuelta, sobre la carne que queramos preparar. En mi caso un solomillo de ternera.</p>

			<p>En una sartén hacemos damos el punto a la carne que deseemos. Y entonces desglasamos los restos que hayan quedado con ayuda del vino de oporto y el brandy o coñac. Lo flambeamos y dejamos que reduzca hasta la mitad.</p>

			<p>Cuando haya reducido hasta la mitad, añadimos la nata y el glas de carne. Y dejamos que espese.El glas de carne es una preparación que se obtiene a partir de un fondo oscuro reducido a fuego lento durante horas y horas hasta que obtenemos una sustancia oscura y muy densa. También hay preparados en el supermercado.</p>

			<p>Al final añadimos unos granos de pimientas molidas y dejamos cocer un par de minutos. Como el glas, suele tener sabor salado, probamos la salsa y afinamos el punto de sal si hiciese falta.</p>
	</div>
</main>