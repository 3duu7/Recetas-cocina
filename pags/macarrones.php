<h1>Macarrones con chorizo y jamon</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 4 personas</p></strong>
				<p>350 g macarrones</p>
				<p>100 g chorizos para guisar</p>
				<p>100 g jamón serrano en daditos</p>
				<p>1 pimiento rojo</p>
				<p>200 g salsa de tomate casera</p>
				<p>100 g queso mozzarella</p>
				<p>orégano</p>
				<p>pimienta</p>
				<p>sal</p>
	</div>

<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
		<p>Retira la piel del chorizo y córtalo en dados. Lava el pimiento, retira el tallo, las semillas y las nervaduras blancas del interior. Córtalo en cuadraditos. Precalienta el horno a 200°.</p>

		<p>Coloca los macarrones, en crudo, en una fuente refractaria. Añade el jamón, el chorizo y el pimiento. Vierte la salsa, espolvorea con orégano, salpimienta y mezcla bien. Vierte 400 ml de agua hirviendo, tapa la fuente con papel sulfurizado y hornea durante 35 minutos.</p>

		<p>Retira la fuente del horno, remueve bien los macarrones y rectifica de sal si es necesario. Coloca  encima la mozzarella cortada en lonchas finas y gratínalos bajo el grill del horno hasta que el queso se funda y la superficie se dore ligeramente. Sirve enseguida.</p>
	</div>
</main>