<h1>Limoncello</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<p>7 limones de cáscara gruesa</p>
			<p>1 litro de alcohol <br>(puede utilizarse aguardiente)</p> 
			<p>1350 gramos de azúcar</p>
			<p>3 litros de agua.</p>
	</div>

<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Se retira la corteza de los limones, con cuidado de no cortarse y tratando que con la corteza quede lo menos posible de la parte blanca interna que recubre los limones. Se ponen las cortezas de limón en un recipiente de cristal con el alcohol y se deja reposar bien cerrado en un sitio oscuro durante 30 días.</p>

			<p>Al cabo de los 30 días se prepara un almíbar suave, por cada litro de agua se utilizan 450 gramos de azúcar). La proporción para que el licor quede con una graduación alcohólica razonable, es 3 litros de almíbar por cada litro de alcohol.</p>

			<p>Una vez hecho el almíbar, se deja enfriar y por último se le agrega el alcohol del que previamente se han filtrado las cortezas de limón. La preparación debe quedar de una tonalidad amarillo verdoso opaco, muy propia del limoncello.</p>
	</div>
</main>