<h1>Pollo con tomate</h1>
<div class="caja1">
	<h2>Ingredientes</h2>
		<strong><p>Para 4 personas</p></strong>
			<p>4 muslos de pollo enteros</p>
			<p>1 cebolla mediana</p>
			<p>½ pimiento rojo</p>
			<p>3 dientes de ajo</p>
			<p>4 tomates maduros</p>
			<p>1 limón</p>
			<p>Aceite de oliva</p>
			<p>Sal y pimienta</p>
</div>

<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Lavar bien los muslos, frotar con limón y separar las patas del cuerpo. Colocar las piezas en un bol y salpimentar bien. Agregar un chorro de aceite y los dientes de ajo machacados. Reservar.</p>
			<p>En un procesador colocar la cebolla, el pimiento y la cebolleta. Procesarlos sin que se vuelvan una pasta, para después conseguir un buen sofrito.</p>
			<p>A continuación, añadir los ingredientes para el sofrito a una cazuela caliente con un poco de aceite. Sofreír hasta que el preparado se ponga tierno o transparente.</p>
			Añadir las piezas de pollo y dorar por todos los lados.</p>
			<p>Mientras tanto, se pelan los tomates, se aplastan con un tenedor y se agregan al guiso una vez el pollo esté bien dorado.</p>
			<p>Después, tapar la olla y cocinar a fuego bajo durante una hora o hasta que el pollo esté cocido.</p>
			<p>Una vez culminado el tiempo, destapar la olla y cocinar unos minutos a fuego algo para lograr que la salsa se reduzca un poco.</p>
	</div>
</main>