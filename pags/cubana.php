		<h1>Arroz a la cubana</h1>
		<h2>Ingredientes</h2>
			<strong><p>Para 1 persona</p></strong>
			<p>400g de Arroz SOS Clásico</p>
			<p>8 salchichas frescas</p>
			<p>2 plátanos</p>
			<p>4 huevos</p>
			<p>400g de salsa de tomate</p>
			<p>2 dientes de ajo</p>
			<p>1 hoja de laurel</p>
			<p>Aceite de oliva</p>
			<p>1 nuez de mantequilla</p>
			<p>Sal</p>
	</div>

<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Cocemos el arroz en abundante agua hirviendo con sal y una gota de aceite durante 13 minutos aproximadamente, si nos gusta que quede muy entero, lo enfriamos poniéndolo bajo el chorro de agua fría y lo dejamos escurrir.</p>

			<p>Mientras el arroz se cuece, vamos dorando las salchichas en una sartén caliente con una gota de aceite y freímos los huevos aparte. Pelamos los plátanos, los cortamos por la mitad a lo largo y los doramos en una sartén antiadherente con un poco de aceite.</p>

			<p>Picamos los ajos y los sofreímos en una sartén con aceite o con mantequilla, añadimos el arroz, lo salteamos y rectificamos de sal.</p>

			<p>Servimos en un plato junto con las salchichas, el huevo y el plátano. Salseamos ligeramente con el tomate y presentamos el plato junto a una salsera.</p>
	</div>
