	<div class="caja1">
		<h2>Ingredientes</h2>
				<p>200 gramos de carne picada <br> (mezcla de cerdo y ternera)</p>
				<p>1 zanahoria</p>
				<p>1 ajo</p>
				<p>1 cebolla</p>
				<p>200 gramos de tomate natural o frito</p>
				<p>1 vaso de vino tinto</p>
				<p>Sal</p>
				<p>Orégano</p>
				<p>Cominos</p>
	</div>

<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Comenzamos pochando el ajo y seguidamente agregamos la cebolla y la zanahoria.Cuando este pochado, agregamos la carne y la doramos. Salpimentamos.</p>

			<p>Con la carne a medio hacer, agregamos el vaso de vino tinto y dejamos que se evapore el alcohol. Seguidamente agregamos el tomate (frito o sin freír bien pelado y sin pepitas) y dejamos cocinar durante unos 15 minutos. En este paso agregamos las especias que queramos, yo le suelo poner orégano y cominos que le van muy bien.</p>
	</div>
</main>