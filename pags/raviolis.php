<h1>Raviolis</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 3 personas</p></strong>		
				<p>400 g de raviolis rellenos de queso</p>
				<p>1 cebolla</p>
				<p>2 dientes de ajo</p>
				<p>½ cucharadita de albahaca seca</p>
				<p>3 quesitos en porciones ligeros</p>
				<p>200 ml de leche evaporada</p>
				<p>Una pizca de nuez moscada</p>
				<p>Pimienta</p>
				<p>25 g de queso parmesano</p>
				<p>Una hojitas de albahaca fresca</p>
	</div>

<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Preparar una olla con abundante agua (los italianos aconsejan 1 litro por cada 100 gramos de pasta), sobre los 4 litros y una cucharadita de sal. Cuando el agua hierva añade la sal y la pasta, cocer la pasta fresca el tiempo recomendado en el paquete, sobre los 4 minutos. Escurrir y reservar.</p>

			<p>Corta la cebolla en juliana y los ajos en varios trozos. Añade aceite de oliva a una sartén y sofríe unos minutos.</p>
			
			<p>Trocear los quesitos en porciones en trocitos y agrega a la sartén. Deja que se fundan a fuego lento. Incorpora la leche evaporada o nata ligera, la pizca de nuez moscada y la pimienta. Deja cocer a fuego lento cinco minutos.</p>

			<p>Traslada esta salsa a un recipiente y pásalo por la batidora hasta lograr una textura cremosa. Combina los raviolis de queso con esta salsa y espolvorea un poco de queso rallado y perejil picado.</p>

			<p>Para evitar que los raviolis se peguen entre sí y no se rompen durante la cocción, espolvoréalos con un poco de harina antes de cocinarlos. Así seguro que te quedan perfectos. </p>
	</div>
</main>