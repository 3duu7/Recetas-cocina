<h1>Ensalada de garbanzos</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 2 personas:</p></strong>
				<p> 300 g de garbanzos cocidos</p>
				<p>1 tomate</p>
				<p>1/4 cebolla morada</p>
				<p>1 aguacate</p>
				<p>1 limón</p>
				<p>100 ml de aceite de oliva virgen extra</p>
				<p>sal</p>
				<p>cilantro fresco.</p>
				<p>1 huevo</p>
		
	</div>

<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Exprimimos el limón y lo mezclamos con el aceite de oliva. Añadimos sal al gusto y batimos hasta emulsionar. </p>

			<p>Reservamos el aliño para el momento de montar y servir la ensalada. Lavamos los garbanzos y los escurrimos bien. </p>

			<p>Lavamos y troceamos el tomate, cortamos el aguacate en dados y picamos la cebolla.</p>
			
			<p>Mezclamos todo con los garbanzos. Aliñamos al gusto y servimos con hojas de cilantro fresco.</p>
	</div>
</main>
