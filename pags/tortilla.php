<h1>Tortilla francesa</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 2 personas</p></strong>
				<p>4 huevos</p>
				<p>2 cucharaditas de aceite de oliva</p>
				<p>2 cucharadas de leche</p>
				<p>sal</p>	
	</div>
	
<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Lo mejor, será hacer 2 tortillas de 2 huevos cada una.</p>

			<p>Casca los huevos (2) y colócalos en un cuenco, sálalos y bátelos bien con un tenedor. Añade las 2 cucharadas de leche para que la tortilla francesa quede más jugosa.</p>

			<p>Pon una cucharadita de aceite a calentar en una sartén, extiéndelo de forma que la base quede bien impregnada. Cuando esté caliente, vierte los huevos batidos, remuévelos un poco.</p>

			<p>Espera unos 15 segundos hasta que empiecen a cuajarse, en ese momento, con cuidado y suavidad, dobla la tortilla por la mitad.</p>

			<p>Voltea la tortilla hacia el otro lado.</p>
	</div>
</main>