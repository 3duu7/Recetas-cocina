<h1>Salsa holandesa</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<p>2 yemas de huevo</p> 
			<p>250 gr de mantequilla clarificada</p>
			<p>2 cucharadas de limón y sal.</p>
	</div>

<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Empezamos montando las yemas, si es un bol al baño maría mejor. Añadimos el zumo de limón. Cuando hayan doblado su volumen iremos añadiendo poco a poco la mantequilla clarificada, logrando una emulsión que se acerque en consistencia a una mayonesa. Por último añadimos la sal.</p>
	</div>
</main>