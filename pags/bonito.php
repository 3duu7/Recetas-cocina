<h1>Marmitako de bonito</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 6 personas</p></strong>
				<p>Bonito 1kg</p>
				<p>Patatas 5</p>
				<p>Cebolla 1</p>
				<p>Pimiento verde italiano 2</p>
				<p>Zanahoria 1</p>
				<p>Dientes de ajo 2</p>
				<p>Caldo de pescado o fumet 1l</p>
				<p>Vino blanco 200 ml</p>
				<p>Pimentón dulce 15 g</p>
				<p>Pimiento choricero una cucharada</p>
				<p>Guindilla 1</p>
				<p>Salsa de tomate 30 g</p>
				<p>Laurel</p>
				<p>Aceite de oliva virgen extra</p>
	</div>
<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Comenzaremos picando la cebolla en juliana, el pimiento en tiras de mediano tamaño, para que se vean, y el ajo muy picado. En una cazuela grande y ancha, que nos permita remover los ingredientes, ponemos un poco de aceite y en él, salteamos la cebolla, el pimiento y el ajo. Bajamos el gas al mínimo, y añadimos la zanahoria, bien picada. Dejamos pochar los ingredientes para que se ablanden bien.</p>

			<p>Ponemos el pimiento choricero en agua caliente, y sacamos su carne con el canto de una cuchara. También podemos utilizar la carne de pimiento choricero en conserva, que da un excelente resultado y resulta muy cómoda de utilizar. La incorporamos al sofrito junto a la guindilla y removemos bien.</p>

			<p>Añadimos el vino blanco, y dejamos que evapore el alcohol. Pelamos las patatas y las cortamos en cachelos, desgajando un poco con el cuchillo para que los pedazos suelten más fécula, lo cual hará que el caldo resultante engorde un poco. Añadimos las patatas al guiso, junto a una punta de cuchillo de pimentón, y un buen pellizco de sal.</p>

			<p>Subimos el gas a fuego fuerte e incorporamos el caldo, las hojas de laurel y las dos cucharadas de tomate, removemos un poco y soltamos la cuchara. Si se desea, se puede poner una ramita de perejil a cocer que después retiraremos. Una vez que el caldo comience a hervir, bajamos de nuevo el gas al mínimo. A partir de ahora removeremos solamente agitando las asas de la cacerola. De esta manera, las patatas permanecerán enteras.</p>

			<p>Dejamos a fuego lento durante unos treinta minutos, hasta que las patatas casi se deshagan, y aprovechamos ese tiempo para repasar los tacos de bonito, cortándolos en trozos de bocado, y retirando alguna espina que puedan tener. Pasados los treinta minutos, añadimos el bonito a la cazuela, apagamos el fuego y dejamos que las tajadas se hagan con el calor residual. Servimos tras cinco minutos de reposo.</p>
