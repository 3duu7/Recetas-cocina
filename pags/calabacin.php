<h1>Crema de calabacín</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 2 personas</p></strong>
				<p>Calabacín 2</p>
				<p>Patata 1</p>
				<p>Cebolla media</p>
				<p>Caldo de verduras 500 ml</p>
				<p>Sal y pimienta al gusto</p>
				<p>Aceite de oliva virgen extra</p>
				<p>Nata líquida 30 ml</p>
				<p>Diente de ajo opcionalmente 1</p>
	</div>	
<main>
	<div class="caja3">
		<h2>Elaboración</h2>
			<p>Cortamos la cebolla en juliana y la sofreímos en una cazuela con un poco de aceite de oliva. Opcionalmente, podemos añadir también un diente de ajo pelado. Dejamos que se poche la cebolla, pero siempre a fuego lento para no que no llegue a tomar mucho color.</p>

			<p>Mientras tanto vamos picando los calabacines, sin pelar y pelamos y cortamos las patatas en trozos irregulares. Cuando la cebolla esté pochada, añadimos el calabacín picado y las patatas a la cazuela. Sofreímos el conjunto ligeramente durante unos diez minutos.</p>

			<p>Traanscurrido ese tiempo rehogando todos los ingredientes a fuego lento, añadimos el caldo de verduras y dejamos que el conjunto cueza otros 20 minutos. Entonces, trituramos con la batidora o pasamos la crema por el pasapurés.</p>

			<p>Reintegramos nuestro guiso a la cazuela y probamos para rectificar de sal y pimienta. Añadimos la nata líquida para que no quede textura espesa de puré sino de crema, dejamos cocer otros cinco minutos más y servimos.</p>
	</div>
</main>