<h1>Lentejas</h1>
	<div class="caja1">
		<h2>Ingredientes</h2>
			<strong><p>Para 4 personas:</p></strong>
				<p>Lentejas 250 g</p>
				<p>Cebolla 80 g</p>
				<p>Patata 250 g</p>
				<p>Salsa de tomate casera 50 g</p>
				<p>Aceite de oliva virgen extra,<br> 6 cucharadas</p>
				<p>Diente de ajo 1</p>
				<p>Laurel</p>
				<p>Sal</p>
				<p>Pimentón dulce ,<br> 1 cucharadita</p>
	</div>
	
<main>
	<div class="caja3">
		<h2>Elaboracion</h2>
			<p>Comenzaremos lavando las lentejas. Por otra parte picamos en trozos menudos la cebolla, la zanahoria, el pimiento verde, el puerro y el tomate si es natural.</p>

			<p>En una olla ponemos tres cucharadas de aceite de oliva a calentar, añadimos las verduras excepto el tomate y las sofreímos a fuego bajo durante cinco minutos, salándolas ligeramente.</p> 

			<p>Añadimos el tomate en dados o bien la salsa de tomate casero y dejamos cocer durante tres minutos más.</p>

			<p>Añadimos las lentejas, la hoja de laurel y las patatas rasgándolas con el cuchillo. Damos unas vueltas con una cuchara de madera y dejamos cocer todo junto dos minutos, añadimos agua fría hasta que cubra las lentejas como dos centímetros por encima.</p>
			
			<p>Salamos y dejamos cocer tapadas durante quince minutos si las hacemos en una olla exprés o bien cincuenta minutos si es en olla tradicional.
			Cuando estén cocidas preparamos una sartén con el aceite restante y lo calentamos. Añadimos el diente de ajo fileteado y lo doramos ligeramente, apartamos del fuego la sartén y le añadimos el pimentón, removiendo y agregando este sofrito a la olla de las Dejamos cocer cinco minutos más y servimos bien calientes.</p>
	</div>
</main>